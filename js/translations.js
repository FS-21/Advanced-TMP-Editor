export let currentLanguage = "en";

export function loadLanguage() {
    const saved = localStorage.getItem("tmp_editor_lang");
    if (saved && TRANSLATIONS[saved]) {
        currentLanguage = saved;
    }
}

export function setLanguage(lang) {
    if (TRANSLATIONS[lang]) {
        currentLanguage = lang;
        localStorage.setItem("tmp_editor_lang", lang);
        applyTranslations();
        window.dispatchEvent(new CustomEvent('languagechange', { detail: { language: lang } }));
    }
}

export function t(key) {
    if (TRANSLATIONS[currentLanguage] && TRANSLATIONS[currentLanguage][key]) {
        return TRANSLATIONS[currentLanguage][key];
    }
    if (TRANSLATIONS["en"] && TRANSLATIONS["en"][key]) {
        return TRANSLATIONS["en"][key];
    }
    return key;
}

export function applyTranslations() {
    state.translations = TRANSLATIONS[currentLanguage] || TRANSLATIONS["en"];

    const elements = document.querySelectorAll('[data-i18n], [data-i18n-html]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n') || el.getAttribute('data-i18n-html');
        const translated = t(key);
        if (translated === key) return;

        const isHtml = el.hasAttribute('data-i18n-html') || (typeof translated === 'string' && translated.includes('<'));
        if (isHtml) {
            el.innerHTML = translated;
            return;
        }

        let found = false;
        for (const child of el.childNodes) {
            if (child.nodeType === Node.TEXT_NODE && child.textContent.trim().length > 0) {
                child.textContent = translated;
                found = true;
                break;
            }
        }

        if (!found) {
            const spans = el.querySelectorAll('span:not(.menu-icon):not(.shortcut):not(.arrow):not(.icon)');
            for (const span of spans) {
                if (!span.hasAttribute('data-i18n') && !span.hasAttribute('data-i18n-html')) {
                    span.textContent = translated;
                    found = true;
                    break;
                }
            }
        }

        if (!found && el.children.length === 0) {
            el.textContent = translated;
        }
    });

    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const translated = t(key);
        if (translated !== key) {
            el.placeholder = translated;
        }
    });

    const titles = document.querySelectorAll('[data-i18n-title]');
    titles.forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        const translated = t(key);
        if (translated !== key) {
            el.setAttribute('data-title', translated);
            el.removeAttribute('title');
        }
    });

    const tooltips = document.querySelectorAll('[data-i18n-tooltip]');
    tooltips.forEach(el => {
        const key = el.getAttribute('data-i18n-tooltip');
        const translated = t(key);
        if (translated !== key) {
            el.setAttribute('data-tooltip', translated);
        }
    });

    // Update the dropdown value to match current state
    const langSelector = document.getElementById('langSelector');
    if (langSelector) {
        langSelector.value = currentLanguage;
    }
}

export function initLanguageSelector() {
    loadLanguage();
    applyTranslations();

    const langSelector = document.getElementById('langSelector');
    if (langSelector) {
        langSelector.value = currentLanguage;
        langSelector.addEventListener('change', (e) => {
            setLanguage(e.target.value);
        });
    }
}
